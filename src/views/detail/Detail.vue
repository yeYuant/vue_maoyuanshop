<script>
import { mapGetters } from "vuex";
import ImageList from "./ImageList/ImageList.vue";
import Zoom from "./zoom/Zoom.vue";
export default {
  name: "Detail",
  components: { ImageList, Zoom },
  data() {
    return {
      skuNum: 1,
    };
  },
  mounted() {
    // 派发actions, 将商品的ID参数给传递过去
    this.$store.dispatch("getDetailList", this.$route.params.skuId);
  },
  computed: {
    ...mapGetters(["categoryView", "skuInfo", "spuSaleAttrList"]),
    // 给子组件Zoom的数据
    skuImageList() {
      return this.skuInfo.skuImageList || [];
    },
  },
  methods: {
    // 点击按钮，将当前商品的售卖属性值设置为高亮  isChecked = 1
    changeActive(saleAttrValue, saleAttr) {
      // 排他思想，先将所有按钮的isChecked = 0 ，去除class
      // forEach()：对数组进行遍历循环，这个方法没有返回值
      // 重新循环一遍saleAttrValue，将所有isChecked = 0
      saleAttr.forEach((item) => {
        item.isChecked = 0;
      });
      // 为当前所点击的 按钮设置 isChecked = 1
      saleAttrValue.isChecked = 1;
    },
    // 表单元素修改产品的个数
    changeSkuNum(event) {
      let value = event.target.value * 1;
      if (isNaN(value) || value < 1) {
        value = 1;
      }

      this.skuNum = parseInt(value);
    },
    // 点击按钮，添加到购物车的回调函数
    async addShopCart() {
      // Promise 中 try catch用来判断异常，如果try 有错误则执行catch ，如果没有则不会执行catch
      try {
        // 本质上下面的代码，是调用了仓库中的 AddOrUpDateShopcart
        // 我们需要知道这次请求是成功还是失败，如果成功进行路由的跳转，如果失败，则需要给用户提示
        await this.$store.dispatch("AddOrUpDateShopcart", {
          skuId: this.$route.params.skuId,
          skuNum: this.skuNum,
        });
        // 进行路由的跳转
        // 我们在确定好商品的数量和各种参数时，在跳转路由的时候需要将数据给传递过去，
        // 但如果我们直接在路由跳转时携带参数，路径会比较难以阅读，所以我们选择使用存储
        // localStorage 和 sessionStorage的区别： 前者是本地存储，后者是会话存储，前者会保留在电脑里面，后者在页面关闭时就会消失
        // 我们选择使用 sessionStorage，因为路径的参数不需要一直存在，只需要当前页面关闭之后消失就行了
        sessionStorage.setItem("SKUINFO", JSON.stringify(this.skuInfo));
        this.$router.push({
          name: "addcartsuccess",
          query: { skuNum: this.skuNum },
        });
      } catch (error) {
        // 失败提示信息
        alert(error.message);
      }
    },
  },
};
</script>

